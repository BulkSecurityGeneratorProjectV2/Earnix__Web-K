package org.xhtmlrenderer.dom.examples;

import org.xhtmlrenderer.dom.Jsoup;
import org.xhtmlrenderer.dom.nodes.Document;
import org.xhtmlrenderer.dom.nodes.Element;
import org.xhtmlrenderer.dom.select.Elements;

import java.io.IOException;

/**
 * A simple example, used on the jsoup website.
 */
public class Wikipedia {
    public static void main(String[] args) throws IOException {
        Document doc = Jsoup.connect("http://en.wikipedia.org/").get();
        log(doc.title());

        Elements newsHeadlines = doc.select("#mp-itn b a");
        for (Element headline : newsHeadlines) {
            log("%s\n\t%s", headline.attr("title"), headline.absUrl("href"));
        }
    }

    private static void log(String msg, String... vals) {
        System.out.println(String.format(msg, vals));
    }
}
